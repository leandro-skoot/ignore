
package com.br.model;


public class Usuarios {
    
    private Integer id_usuarios;
    private String login_usuarios;
    private String senha_usuarios;
    private String perfil_usuarios;
    private String email_usuarios;

    public Integer getId_usuarios() {
        return id_usuarios;
    }

    public void setId_usuarios(Integer id_usuarios) {
        this.id_usuarios = id_usuarios;
    }

    public String getLogin_usuarios() {
        return login_usuarios;
    }

    public void setLogin_usuarios(String login_usuarios) {
        this.login_usuarios = login_usuarios;
    }

    public String getSenha_usuarios() {
        return senha_usuarios;
    }

    public void setSenha_usuarios(String senha_usuarios) {
        this.senha_usuarios = senha_usuarios;
    }

    public String getPerfil_usuarios() {
        return perfil_usuarios;
    }

    public void setPerfil_usuarios(String perfil_usuarios) {
        this.perfil_usuarios = perfil_usuarios;
    }

    public String getEmail_usuarios() {
        return email_usuarios;
    }

    public void setEmail_usuarios(String email_usuarios) {
        this.email_usuarios = email_usuarios;
    }
    
    
    
}
